    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (check()) {
            try {
                TaiKhoan tk = getInputForm();
                if (tks.addTK(tk) != null) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");

                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
                loadData();
            } catch (Exception e) {
            }
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (check()) {
            int index = tblTaiKhoan.getSelectedRow();
            int id = list.get(index).getId();
            try {
                TaiKhoan tk = getInputForm();
                tk.setId(id);
                if (tks.updateTK(tk) != null) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                    loadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa thất bại");
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int chon = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn xóa?", "Xóa", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (chon == JOptionPane.YES_OPTION) {
            try {
                int index = tblTaiKhoan.getSelectedRow();
                int id = list.get(index).getId();
                if (tks.deleteTK(id) != null) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    loadData();
                    clearForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại");
                }

            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

