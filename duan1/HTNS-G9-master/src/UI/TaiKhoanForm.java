    private void loadData() {
        mol = (DefaultTableModel) tblTaiKhoan.getModel();
        mol.setRowCount(0);
        list = tks.getAll();
        for (TaiKhoan tk : list) {
            mol.addRow(new Object[]{
                tk.getId(),
                tk.getMaTK(),
                tk.getPass(),
                tk.isVaitro() ? "Quản Lý" : "Nhân Viên"
            });
        }
    }

    private void showDetail() {
        int index = tblTaiKhoan.getSelectedRow();
        lblid.setText(tblTaiKhoan.getValueAt(index, 0).toString());
        txttk.setText(tblTaiKhoan.getValueAt(index, 1).toString());
        txtPass.setText(tblTaiKhoan.getValueAt(index, 2).toString());
        boolean gt = tblTaiKhoan.getValueAt(index, 3).toString().equalsIgnoreCase("Quản Lý");
        rdoQuanly.setSelected(gt);
        rdoNhanvien.setSelected(!gt);
    }

    private TaiKhoan getInputForm() {
        TaiKhoan tk = new TaiKhoan();
        tk.setMaTK(txttk.getText());
        tk.setPass(txtPass.getText());
        tk.setVaitro(rdoQuanly.isSelected() ? true : false);
        return tk;
    }

    private void clearForm() {
        txttk.setText("");
        txtPass.setText("");
        rdoQuanly.setSelected(true);
        lblid.setText("0");
    }

    private boolean check() {
        if (txttk.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền mã nhân viên");
            txttk.requestFocus();
            return false;
        }
        list = tks.getAll();
        for (TaiKhoan tk : list) {
            if (txttk.getText().trim().equalsIgnoreCase(tk.getMaTK())) {
                // Mã sinh viên đã tồn tại
                JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại, vui lòng kiểm tra lại!");
                txttk.requestFocus();
                return false; // Không cho phép thêm mới
            }
        }
        if (txtPass.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền mật khẩu");
            txtPass.requestFocus();
            return false;
        }
        return true;
    }
